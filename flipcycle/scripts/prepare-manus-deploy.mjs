import { cp, mkdir, rm, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const frontendDir = path.join(root, 'frontend');
const nextDir = path.join(frontendDir, '.next');
const standaloneDir = path.join(nextDir, 'standalone');
const distDir = path.join(root, 'dist');

if (!existsSync(standaloneDir)) {
  throw new Error('Missing frontend/.next/standalone. Ensure frontend/next.config.ts sets output: "standalone" and run pnpm build first.');
}

await rm(distDir, { recursive: true, force: true });
await mkdir(distDir, { recursive: true });
await cp(standaloneDir, distDir, { recursive: true });

const standaloneFrontendDir = path.join(distDir, 'frontend');
const standaloneServer = path.join(standaloneFrontendDir, 'server.js');
if (!existsSync(standaloneServer)) {
  throw new Error('Missing standalone frontend/server.js after copying the Next.js build output.');
}

await mkdir(path.join(standaloneFrontendDir, '.next'), { recursive: true });
await cp(path.join(nextDir, 'static'), path.join(standaloneFrontendDir, '.next', 'static'), { recursive: true });

const publicDir = path.join(frontendDir, 'public');
if (existsSync(publicDir)) {
  await cp(publicDir, path.join(standaloneFrontendDir, 'public'), { recursive: true });
}

const launcher = `// Generated by scripts/prepare-manus-deploy.mjs.\n// Manus Cloud Run starts node dist/index.js, while the migrated app is a Next.js standalone server.\nrequire('./frontend/server.js');\n`;
await writeFile(path.join(distDir, 'index.js'), launcher, 'utf8');

console.log('Prepared Manus deployment artifact at dist/index.js using Next.js standalone output.');
